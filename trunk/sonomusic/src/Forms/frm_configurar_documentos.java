    private void t_documentosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_t_documentosMouseClicked
        if (evt.getClickCount() == 2) {
            int i = t_documentos.getSelectedRow();
            txt_ser.setText(t_documentos.getValueAt(i, 2).toString());
            txt_nro.setText(t_documentos.getValueAt(i, 3).toString());
            txt_ser.requestFocus();
            txt_ser.setEnabled(true);
            txt_nro.setEnabled(true);
            btn_mod.setEnabled(true);
            id = Integer.parseInt(t_documentos.getValueAt(i, 0).toString());
        }
    }//GEN-LAST:event_t_documentosMouseClicked

    private void btn_modActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modActionPerformed
        try {
            Statement st = con.conexion();
            String upd = "update documento_almacen set ser_doc = '" + txt_ser.getText() + "', nro_doc = '" + txt_nro.getText() + "' where "
                    + "idtipo_doc = '" + id + "' and idalmacen = '" + frm_menu.alm.getId()+ "'";
            con.actualiza(st, upd);
            con.cerrar(st);
            btn_mod.setEnabled(false);
            txt_ser.setText("");
            txt_nro.setText("");
            txt_ser.setEnabled(false);
            txt_nro.setEnabled(false);
            cargar_doc();
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, e.getLocalizedMessage());
        }
    }//GEN-LAST:event_btn_modActionPerformed


