    private void cargar_doc() {
        modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int fila, int columna) {
                return false;
            }
        };
        modelo.addColumn("Id");
        modelo.addColumn("Descripcion");
        modelo.addColumn("Serie");
        modelo.addColumn("Nro");
        try {
            String query = "select td.idtipo_doc, td.desc_tipd as tipo_documento, da.ser_doc, da.nro_doc from documento_almacen as da inner join "
                    + "tipo_doc as td on da.idtipo_doc = td.idtipo_doc where da.idAlmacen = '"+frm_menu.alm.getId()+"'";
            Statement st = con.conexion();
            ResultSet rs = con.consulta(st, query);
            Object fila[] = new Object[4];
            while (rs.next()) {
                fila[0] = rs.getString("idtipo_doc");
                fila[1] = rs.getString("tipo_documento");
                fila[2] = rs.getString("ser_doc");
                fila[3] = rs.getString("nro_doc");
                /*String tipo = rs.getString("tipo_documento");
                if (tipo.equals("F")) {
                    fila[4] = "FISICO";
                }
                if (tipo.equals("E")) {
                    fila[4] = "ELECTRONICO";
                }*/
                modelo.addRow(fila);
            }
            t_documentos.setModel(modelo);
            t_documentos.getColumnModel().getColumn(0).setPreferredWidth(30);
            t_documentos.getColumnModel().getColumn(1).setPreferredWidth(100);
            t_documentos.getColumnModel().getColumn(2).setPreferredWidth(50);
            t_documentos.getColumnModel().getColumn(3).setPreferredWidth(70);
            ven.centrar_celda(t_documentos, 2);
            ven.centrar_celda(t_documentos, 3);
            con.cerrar(rs);
            con.cerrar(st);
        } catch (SQLException ex) {
            System.out.print(ex);
            JOptionPane.showMessageDialog(null, ex.getLocalizedMessage());
        }

    }

