    private void ver_almacen(String query) {
        try {
            Statement st = con.conexion();
            ResultSet rs = con.consulta(st, query);
            
            while (rs.next()) {
                String fila;
                fila = rs.getString("nom_alm");
                cbx_alm_de.addItem(fila);
            }
            con.cerrar(st);
            con.cerrar(rs);
        } catch (SQLException e) {
            System.out.print(e);
        }
    }
    
